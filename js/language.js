function mudarIdioma(idioma) {
    // NavBAar
    document.getElementById('language-sobre').textContent = textos[idioma].navBar.sobre;
    document.getElementById('language-projeto').textContent = textos[idioma].navBar.projeto;
    document.getElementById('language-projeto-dropdown').textContent = textos[idioma].navBar.projeto_dropdown;
    document.getElementById('language-equipe').textContent = textos[idioma].navBar.equipe;
    document.getElementById('language-contato').textContent = textos[idioma].navBar.contato;

    // Sobre Nós
    document.getElementById('language-sobre-title').textContent = textos[idioma].sobreNos.sobre_title;
    document.getElementById('texto-sobre-p1').textContent = textos[idioma].sobreNos.sobre_texto_pt1;
    document.getElementById('texto-sobre-p2').textContent = textos[idioma].sobreNos.sobre_texto_pt2;
    document.getElementById('texto-sobre-p3').textContent = textos[idioma].sobreNos.sobre_texto_pt3;

    // Projetos
    document.getElementById('language-projeto-title').textContent = textos[idioma].projetos.projeto_title;

    // Equipe title
    document.getElementById('language-equipe-title-p1').textContent = textos[idioma].equipe.equipe_title_p1;
    document.getElementById('language-equipe-title-p2').textContent = textos[idioma].equipe.equipe_title_p2;
    document.getElementById('language-equipe-title-p3').textContent = textos[idioma].equipe.equipe_title_p3;

    // Mauricio
    document.getElementById('language-cargo-mauricio').textContent = textos[idioma].mauricio.cargo;
    document.getElementById('language-descricao-mauricio').textContent = textos[idioma].mauricio.descricao;
    
    // Gabriel
    document.getElementById('language-cargo-gabriel').textContent = textos[idioma].gabriel.cargo;
    document.getElementById('language-descricao-gabriel').textContent = textos[idioma].gabriel.descricao;

    // Pedro
    document.getElementById('language-cargo-pedro').textContent = textos[idioma].pedro.cargo;
    document.getElementById('language-descricao-pedro').textContent = textos[idioma].pedro.descricao;

    // Felipe
    document.getElementById('language-cargo-felipe').textContent = textos[idioma].felipe.cargo;
    document.getElementById('language-descricao-felipe').textContent = textos[idioma].felipe.descricao;

    // Gabriel Barberiz
    document.getElementById('language-cargo-gabrielBarberiz').textContent = textos[idioma].gabrielBarberiz.cargo;
    document.getElementById('language-descricao-gabrielBarberiz').textContent = textos[idioma].gabrielBarberiz.descricao;


    // contato form
    document.getElementById('language-contato-title').textContent = textos[idioma].contato_form.contato_title;
    document.getElementById('language-contato-desc').textContent = textos[idioma].contato_form.contato_desc;
    document.getElementById('language-inp-name').textContent = textos[idioma].contato_form.input_nome;
    document.getElementById('language-inp-email').textContent = textos[idioma].contato_form.input_email;
    document.getElementById('language-inp-mensagem').textContent = textos[idioma].contato_form.input_mensagem;
    document.getElementById('language-enviar').textContent = textos[idioma].contato_form.enviar;

}

